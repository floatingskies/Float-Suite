  /* ---- Public API ---------------------------------------------------- */

  /** Detect the best initial language from storage, then browser. */
  function detect(){
    try{
      const stored = localStorage.getItem(STORAGE_KEY);
      if(stored && SUPPORTED.indexOf(stored)>=0) return stored;
    }catch(e){}
    const nav = (navigator.language || navigator.userLanguage || 'en');
    if(SUPPORTED.indexOf(nav)>=0) return nav;
    const base = nav.split('-')[0];
    const match = SUPPORTED.find(l => l.indexOf(base)===0);
    return match || FALLBACK;
  }

  let _lang = detect();
  let _listeners = [];

  function t(key, vars){
    let str = (D[_lang] && D[_lang][key]) || (D[FALLBACK] && D[FALLBACK][key]) || key;
    if(vars && typeof str === 'string'){
      Object.keys(vars).forEach(k => {
        str = str.split('{'+k+'}').join(vars[k]);
      });
    }
    return str;
  }

  function setLang(lang){
    if(SUPPORTED.indexOf(lang)<0) lang = FALLBACK;
    if(lang === _lang) return;
    _lang = lang;
    try{ localStorage.setItem(STORAGE_KEY, lang); }catch(e){}
    apply();
    _listeners.forEach(fn => { try{fn(lang);}catch(e){} });
    document.dispatchEvent(new CustomEvent('float:langchange', {detail:{lang}}));
  }

  function getLang(){ return _lang; }
  function supported(){ return SUPPORTED.slice(); }

  function apply(){
    const els = document.querySelectorAll('[data-i18n]');
    els.forEach(el => {
      const key = el.getAttribute('data-i18n');
      const txt = t(key);
      if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA'){
        if(el.hasAttribute('placeholder')) el.setAttribute('placeholder', txt);
        else el.value = txt;
      }else if(el.hasAttribute('data-i18n-attr')){
        const pairs = el.getAttribute('data-i18n-attr').split(',');
        pairs.forEach(p => {
          const [attr,k] = p.split(':').map(s=>s.trim());
          if(attr && k) el.setAttribute(attr, t(k));
        });
      }else{
        el.textContent = txt;
      }
    });
    document.documentElement.setAttribute('lang', _lang.split('-')[0]);
  }

  function on(fn){
    if(typeof fn === 'function') _listeners.push(fn);
    return () => { _listeners = _listeners.filter(f => f !== fn); };
  }

  global.FloatI18n = {
    t, setLang, getLang, supported, apply, on,
    get dict(){ return D; }
  };

